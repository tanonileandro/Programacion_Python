
a = 5
b = 5

resultado = (a == b)  # si los valores son iguales
print(f'Resultado == : {resultado}')

resultado = (a != b)  # si los valores son distintos
print(f'Resultado != : {resultado}')

resultado = ( a > b)
print(f'Resultado  a > b: {resultado}')

resultado = ( a < b)
print(f'Resultado  a < b: {resultado}')

resultado = ( a >= b)
print(f'Resultado  a >= b: {resultado}')

resultado = ( a <= b)
print(f'Resultado  a <= b: {resultado}')