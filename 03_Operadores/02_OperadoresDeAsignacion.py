miVariable = 5
print(miVariable)

# Incrementar la variable
miVariable = miVariable + 1
print(miVariable)

miVariable = 5
print(miVariable)

# Incrementar la variable de forma mas simple
miVariable += 1  # sirve para reasignar un valor, es igual a (miVariable = miVariable + 1)
print(miVariable)

# Producto de la variable anterior
miVariable *= 2  # es = a (miVariable = miVariable * 2)
print(miVariable)

miVariable /= 2
print(miVariable)
