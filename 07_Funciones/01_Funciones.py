def miFuncion(nombre, apellido):  # Entre corchetes se indico los parametros de la funcion
    print('Saludos desde mi funcion...')
    print(f'Nombre y Apellido: {nombre} {apellido}')


miFuncion('Leandro', 'Tanoni')  # Para que se imprima en pantalla, debemos llamarla a la funcion..
# Entre corchetes se indico los argumentos de la funcion, es informacion necesaria
# debido a que agregamos dos parametros anteriormente, los parametros y argumentos van de la mano
miFuncion('Carlos', 'Tanoni')