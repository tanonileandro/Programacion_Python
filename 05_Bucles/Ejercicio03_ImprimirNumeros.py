# Imprimir numeros del 0 al 5

numMax = 5
cont = 0

while cont <= numMax:
    print(cont)
    cont += 1 # cont = cont + 1
else:
    print('Fin ciclo while\n')

# Imprimir numeros del 5 al 0

numMin = 0
cont = 5

while cont >= numMin:
    print(cont)
    cont -= 1 # cont = cont - 1
else:
    print('Fin ciclo while')