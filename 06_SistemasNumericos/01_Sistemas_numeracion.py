# Representaicon en Decimal a = 10
a = 10
print(f'a: {a}')

# Representacion en Binario de  a = 10
a = 0b1010
print(f'a: {a}')

# Representacion en Octal de a = 10
a = 0o12
print(f'a: {a}')

# Representacion en Hexadecimal de a = 10
a = 0xA
print(f'a: {a}')

# Convertir un tipo entero, incluyendo base, a = 23
# Base Decimal
a = int('23', 10)
print(f'a: {a}')

# Base Binario
a = int('10111', 2)
print(f'a: {a}')

# Base Octal
a = int('27', 8)
print(f'a: {a}')

# Base Hexadecimal
a = int('17', 16)
print(f'a: {a}')